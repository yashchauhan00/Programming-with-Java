//How many times Hello is printed.
import java.util.*;
public class Q7 {
    public static void main(String args[]){
        Scanner sc= new Scanner(System.in);
        int n;
        System.out.print("Enter the num=");
        n=sc.nextInt();
        for(int i=1; i<=n; i++)
        {
            System.out.println("Hello");
        }
        System.out.println();
    }
    
}
